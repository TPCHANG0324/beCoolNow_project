<script>
import { ref, computed } from 'vue';
import { useRouter } from 'vue-router';
import MainHeader from '@/components/layout/MainHeader.vue';
import MainFooter from '@/components/layout/MainFooter.vue';
import BreadcrumbNavigation from '@/components/BreadcrumbNavigation.vue';
import { PRICE_RANGES } from '@/store/priceRanges.js';

export default {
  name: 'ProductChoice',
  components: {
    MainHeader,
    MainFooter,
    BreadcrumbNavigation
  },

  setup() {
    const router = useRouter();
    const showCartPopup = ref(false);
    const quantity = ref(1);
    const selectedSize = ref('');
    const sizes = ref(['200ml', '350ml', '500ml', '750ml']);
    const productPrice = ref(299);

    // è¨ˆç®—ç•¶å‰åƒ¹æ ¼å€é–“
    const currentPriceRange = computed(() => {
      const range = PRICE_RANGES.find(
        range => productPrice.value > range.min && productPrice.value <= range.max
      );
      return range ? range.value : 'NT$300ä»¥ä¸Š';
    });

    // æ•¸é‡ç›¸é—œæ–¹æ³•
    const incrementQuantity = () => {
      if (quantity.value < 99) {
        quantity.value++;
      }
    };

    const decrementQuantity = () => {
      if (quantity.value > 1) {
        quantity.value--;
      }
    };

    const handleQuantityInput = (event) => {
      let value = parseInt(event.target.value);
      if (isNaN(value) || value < 1) {
        quantity.value = 1;
      } else if (value > 99) {
        quantity.value = 99;
      } else {
        quantity.value = value;
      }
    };

    // è³¼ç‰©è»Šç›¸é—œæ–¹æ³•
    const updateCartCount = (count) => {
      const currentCount = parseInt(localStorage.getItem('cartCount')) || 0;
      const newCount = currentCount + count;
      localStorage.setItem('cartCount', newCount.toString());
      window.dispatchEvent(new Event('updateCartCount'));
    };

    const addToCart = () => {
      if (!selectedSize.value) {
        alert('è«‹é¸æ“‡å•†å“è¦æ ¼');
        return;
      }

      // å»ºç«‹è³¼ç‰©è»Šé …ç›®
      const cartItem = {
        id: Date.now(), // è‡¨æ™‚ID
        name: 'å†ç”Ÿæè³ªç’°ä¿æ¯',
        price: productPrice.value,
        size: selectedSize.value,
        quantity: quantity.value,
        image: '/assets/images/Sp15.jpg'
      };

      // ç²å–ç¾æœ‰è³¼ç‰©è»Šé …ç›®
      const cartItems = JSON.parse(localStorage.getItem('cartItems')) || [];
      cartItems.push(cartItem);
      localStorage.setItem('cartItems', JSON.stringify(cartItems));

      // æ›´æ–°è³¼ç‰©è»Šæ•¸é‡
      updateCartCount(quantity.value);
      
      // é¡¯ç¤ºæˆåŠŸå½ˆçª—
      showCartPopup.value = true;
    };

    const closeCartPopup = () => {
      showCartPopup.value = false;
    };

    const goToCart = () => {
      router.push('/shop_cart');
    };

    return {
      showCartPopup,
      quantity,
      selectedSize,
      sizes,
      productPrice,
      currentPriceRange,
      incrementQuantity,
      decrementQuantity,
      handleQuantityInput,
      addToCart,
      closeCartPopup,
      goToCart
    };
  }
};
